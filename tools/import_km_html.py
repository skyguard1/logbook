# <img> with these class tokens are decoration (avatar / QR / KM iconography)
# and should be removed entirely instead of being copied as assets.
IMAGE_DECORATION_CLASS_PATTERNS = [
    r"\bavatar\b",
    r"\bhas-apply-img\b",
    r"\bkm[_-]?headline\b",
    r"\bheadline[_-]?icon\b",
    r"\bqr[_-]?code\b",
    r"\bsprite\b",
    r"\bicon-",
]

IMG_TAG_RE = re.compile(r"<img\b[^>]*?/?>", re.IGNORECASE)
IMG_SRC_RE = re.compile(r'src\s*=\s*"([^"]*)"', re.IGNORECASE)
IMG_CLASS_RE = re.compile(r'class\s*=\s*"([^"]*)"', re.IGNORECASE)

# Magic-byte sniffing so extension-less files (e.g. cos-file-url(3)) get the
# right suffix when copied into the Hexo asset tree.
_IMAGE_MAGIC: list[tuple[bytes, str]] = [
    (b"\x89PNG\r\n\x1a\n", "png"),
    (b"\xff\xd8\xff", "jpg"),
    (b"GIF87a", "gif"),
    (b"GIF89a", "gif"),
    (b"BM", "bmp"),
    (b"<svg", "svg"),
    (b"<?xml", "svg"),
]


def sniff_image_ext(data: bytes) -> Optional[str]:
    head = data[:32]
    for magic, ext in _IMAGE_MAGIC:
        if head.startswith(magic):
            return ext
    if head.startswith(b"RIFF") and data[8:12] == b"WEBP":
        return "webp"
    return None


def _normalize_code_blocks(content: str) -> str:
    """Flatten KM-flavored <pre><code>…</code></pre> blocks.

    KM exports color the source by wrapping every token in nested
    <span style="color:…">…</span>. hexo-renderer-marked treats anything
    inside <pre><code> as raw code text and HTML-escapes it, so those spans
    end up rendered as literal `<span style=...>` strings on the site.

    Strip every inline <span> (keeping text), and clear inline style/class on
    <pre> and inner <code> so the theme's syntax styles can apply cleanly.
    """

    def _clean(match: re.Match) -> str:
        inner = match.group(1)
        # Drop nested spans (KM nests them deeply).
        prev = None
        while prev != inner:
            prev = inner
            inner = re.sub(r"<span\b[^>]*>", "", inner, flags=re.IGNORECASE)
            inner = re.sub(r"</span>", "", inner, flags=re.IGNORECASE)
        # Convert <br> to a literal newline so it survives marked escaping.
        inner = re.sub(r"<br\s*/?>", "\n", inner, flags=re.IGNORECASE)
        # Normalize <code ...> -> <code>
        inner = re.sub(r"<code\b[^>]*>", "<code>", inner, flags=re.IGNORECASE)
        # Strip any remaining inline tags (e.g. <font>, <b>, <i>, <em>, <a>);
        # keep <code>/</code> intact.
        inner = re.sub(
            r"<(?!/?code\b)/?[a-zA-Z][^>]*>",
            "",
            inner,
            flags=re.IGNORECASE,
        )
        return f"<pre>{inner}</pre>"

    return re.sub(r"<pre\b[^>]*>([\s\S]*?)</pre>", _clean, content, flags=re.IGNORECASE)


