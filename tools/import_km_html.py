def _detect_site_root() -> str:
    """Read site root from _config.yml so generated <img src> works whether the
    Hexo site is served at / or under a subpath (e.g. GitHub Pages /logbook/).
    Returns a string starting and ending with '/'.
    """
    config = REPO_ROOT / "_config.yml"
    root = "/"
    if config.is_file():
        text = config.read_text(encoding="utf-8", errors="ignore")
        m = re.search(r"^root:\s*(\S+)", text, re.MULTILINE)
        if m:
            root = m.group(1).strip().strip('"').strip("'")
        else:
            m = re.search(r"^url:\s*(\S+)", text, re.MULTILINE)
            if m:
                url = m.group(1).strip().strip('"').strip("'")
                path = re.sub(r"^[a-zA-Z]+://[^/]+", "", url)
                root = path or "/"
    if not root.startswith("/"):
        root = "/" + root
    if not root.endswith("/"):
        root = root + "/"
    return root


